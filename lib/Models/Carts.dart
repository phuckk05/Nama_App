class CartItem {
  String idCart;
  String idProduct;
  String name;
  String description;
  String address;
  String email;
  String imageUrl;
  String type;
  String price;
  String total;
  String createdAt;
  String emailAdd;

  CartItem({
    required this.idCart,
    required this.idProduct,
    required this.name,
    required this.description,
    required this.address,
    required this.email,
    required this.imageUrl,
    required this.type,
    required this.price,
    required this.total,
    required this.createdAt,
    required this.emailAdd,
  });
}
